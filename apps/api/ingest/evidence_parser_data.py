# Quran patterns
QURAN_PATTERN_BRACKET = re.compile(
    r"[﴿{]([^﴾}]+)[﴾}]\s*[\[\(]([^:\]]+)[:\s]+(\d+)(?:\s*[-–]\s*(\d+))?\s*[\]\)]",
    re.UNICODE,
)

QURAN_PATTERN_REF_ONLY = re.compile(
    r"[\[\(]([^:\]]+)[:\s]+(\d+)(?:\s*[-–]\s*(\d+))?\s*[\]\)]",
    re.UNICODE,
)

QURAN_PATTERN_BARE = re.compile(
    r"([\u0600-\u06FF\s]+)[:\s]+(\d+)(?:\s*[-–]\s*(\d+))?",
    re.UNICODE,
)


