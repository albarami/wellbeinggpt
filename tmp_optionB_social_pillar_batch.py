def write(stem: str, lines: list[str], *, pillar: str, core: str|None=None, sub: str|None=None):
    joined='\n'.join(lines).strip()+'\n'
    sha=hashlib.sha256(joined.encode('utf-8')).hexdigest()
    ctx={'pillar_name_ar': pillar}
    if core:
        ctx['core_value_name_ar']=core
    if sub:
        ctx['sub_value_name_ar']=sub
    payload={
        'source_file_hash': DOC_HASH,
        'image_sha256': sha,
        'filename': f"{stem}.png",
        'lines': [ln for ln in lines if ln.strip()],
        'context': ctx,
    }
    out=base/f"{sha[:16]}_{stem}.json"
    out.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding='utf-8')
    print('wrote', out)

